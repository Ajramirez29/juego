    private void playActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playActionPerformed
        // TODO add your handling code here:
        if (ContadorVidas>0){
        text1.setText(Integer.toString(rondas+1));
        rondas= rondas+1;
        vidasr.setText(Integer.toString(ContadorVidas));
        int d1 =rand.nextInt(5)+1;
        numero1=d1;
        int d2 =rand.nextInt(5)+1;
        numero2=d2;
        int d3 =rand.nextInt(5)+1;
        numero3=d3;
        text2.setText(Integer.toString(d1));
        text3.setText(Integer.toString(d2));
        text4.setText(Integer.toString(d3));  
        }
        else {
        JOptionPane.showMessageDialog(null,"Usted ya no tiene Vidas"+"\nNumero de Rondas: "+rondas);
        }
    }//GEN-LAST:event_playActionPerformed

